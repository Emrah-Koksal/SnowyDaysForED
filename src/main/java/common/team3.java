package common;

public class team3 {
    //Write a Java Program to find whether a number is prime or not.
    public static void main(String[] args) {
        int num = 0;
        if (num%2!=0 || num>=2) {
            System.out.println("This is a prime number");
        }else {
            System.out.println("This is not a prime number");
        }
    }
}
