package FatihPackage;

public class task001 {

    /**
     * Write a Java Program to swap two numbers using the third variable.
     */
    public static void main(String[] args) {

        int x = 10;
        int y = 5;

        int temp = x;
        x = y;
        y = temp;

        System.out.println("x: " + x);
        System.out.println("y: " + y);

    }

}
